/* Using Arrow function */
const pentagonalNumber=(n)=> {
	return (n *((3* n)-1)) / 2;
}
console.log(pentagonalNumber(1));
console.log(pentagonalNumber(5));
console.log(pentagonalNumber(12));
console.log(pentagonalNumber(22));



	